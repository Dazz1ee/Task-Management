CREATE TABLE IF NOT EXISTS review (
    review_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_id int REFERENCES task(task_id) ON DELETE CASCADE,
    owner_id int REFERENCES users(user_id) ON DELETE CASCADE,
    status bool,
    message text,
    UNIQUE (task_id, owner_id)
);

CREATE INDEX IF NOT EXISTS review_task_id_index ON review(task_id);
CREATE INDEX IF NOT EXISTS review_owner_id_index ON review(owner_id);
CREATE INDEX IF NOT EXISTS review_review_id_index ON review(review_id);